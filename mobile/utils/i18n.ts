export const i18n = new I18n(translations);
i18n.defaultLocale = "en";
i18n.locale = "en";
i18n.enableFallback = true;

export const setLocale = async (locale: "en" | "am") => {
  i18n.locale = locale;
  await SecureStore.setItemAsync("app_language", locale);
};

export const loadLocale = async () => {
  const saved = await SecureStore.getItemAsync("app_language");
  if (saved === "en" || saved === "am") i18n.locale = saved;
};

export const t = (key: string, options?: object) => i18n.t(key, options);
